#include "game.h"

int			get_dir(t_map map)
{
	int		x;
	int		y;

	y = 0;
	while (y < map.height)
	{
		x = 0;
		while (map.map[y][x])
		{
			if (map.map[y][x] >= 'A' && map.map[y][x] <= 'Z')
				return (map.map[y][x]);
			x++;
		}
		y++;
	}
	return (0);
}
